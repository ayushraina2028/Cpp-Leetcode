#include <iostream>
using namespace std;



int main() {
    
    // to get sizes of data types
    // integer
    cout << sizeof(int) << endl;
    
    // char
    cout << sizeof(char) << endl;
    
    // float
    cout << sizeof(float) << endl;
    
    // bool
    cout << sizeof(bool) << endl;
   
    // long long int
    cout << sizeof(long long int) << endl;


}